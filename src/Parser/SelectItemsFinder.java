	@Override
	public void visit(PlainSelect arg0) {
		// TODO Auto-generated method stub
		List list = arg0.getSelectItems();
		for (int i = 0; i < list.size(); i++) {
			SelectExpressionItem exprItem = (SelectExpressionItem) list.get(i);
			if(exprItem.getExpression() instanceof Function){
				return;
			}
			String ans = "" + list.get(i);
			selectItemsList.add(ans);
		}
	}

